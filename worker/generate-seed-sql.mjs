// One-time codegen: turns fakeData.js's seed-only data (payment policies, schedule-change
// events, task-done dates) into ready-to-paste SQL for Cloudflare D1's console. Not part of the
// Worker itself - run manually during setup (see README.md), output committed to
// migrations/seed.sql for reproducibility.
import { buildWorld } from './src/fakeData.js';

const now = new Date();
const { paymentPolicies, scheduleChangeEvents, taskDoneDates } = buildWorld(now);

const esc = (s) => (s == null ? 'NULL' : `'${String(s).replace(/'/g, "''")}'`);
const lines = [];

lines.push('-- One-time seed for the demo dataset - generated by worker/generate-seed-sql.mjs.');
lines.push('-- Fictitious data only. Paste into the D1 console AFTER running all 9 schema migrations.');
lines.push('');

paymentPolicies.forEach((p) => {
  const milestonesJson = JSON.stringify(p.milestones).replace(/'/g, "''");
  lines.push(
    `INSERT INTO payment_policies (project_id, reviewed, milestones_json, hour_bank_size) VALUES (${esc(p.projectId)}, 1, '${milestonesJson}', ${p.hourBankSize ?? 'NULL'});`
  );
});

lines.push('');
scheduleChangeEvents.forEach((e) => {
  lines.push(
    `INSERT INTO schedule_change_events (project_id, changed_at, new_end_date) VALUES (${esc(e.projectId)}, ${esc(e.changedAt)}, ${esc(e.newEndDate)});`
  );
});

lines.push('');
taskDoneDates.forEach((d) => {
  lines.push(
    `INSERT INTO task_status_history (task_id, done_at) VALUES (${esc(d.taskId)}, ${esc(d.doneAt)});`
  );
});

console.log(lines.join('\n'));
